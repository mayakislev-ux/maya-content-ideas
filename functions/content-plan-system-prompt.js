const CONCISENESS_RULE = `**קריטי - תמציתיות:** אל תוסיפי שום שדה או טקסט מעבר לפורמט המבוקש. שדה "note" (אם משתמשים בו) - משפט אחד קצר בלבד.`;

function buildContentPlanPrompt({ weeksCount, postsPerWeek, liveContentNote, ideas }) {
  const ideasBlock = ideas
    .map((idea, i) => `${i + 1}. "${idea.title}" | קטגוריה: ${idea.category || 'לא מסווג'}${idea.persuasionStage ? ` | שלב שכנוע: ${idea.persuasionStage}` : ''}${idea.rating ? ` | דירוג: ${idea.rating}` : ''}`)
    .join('\n');

  const liveBlock =
    liveContentNote && liveContentNote.trim()
      ? `\n\nתוכן חי (לייבים/הופעות/פודקאסטים וכו') שהמשתמשת ציינה שקורה בתקופה הזו ולא נכתב באפליקציה בכלל - שבצי את זה כפריטים נפרדים ("type":"live") במקומות סבירים בתכנית, מפוזר, לא הכל ביחד. תארי כל פריט live בקצרה על סמך מה שהמשתמשת כתבה, בלי להמציא פרטים שהיא לא ציינה:\n${liveContentNote.trim()}`
      : '';

  return `את/ה עוזר/ת תוכן שבונה תכנית תוכן חודשית לבעלת עסק (לקוחת "אקדמיית המהלך השיווקי"), אך ורק מתוך רשימת רעיונות שכבר קיימת אצלה במאגר האפליקציה - את/ה לא ממציא/ה רעיונות חדשים כאן, רק משבצ/ת את מה שכבר יש בלוח זמנים הגיוני.

המשימה: לבנות תכנית תוכן ל-${weeksCount} שבועות, יעד של ${postsPerWeek} פריטי תוכן כתובים בשבוע מתוך הרעיונות שסופקו למטה (סה"כ יעד של ${weeksCount * postsPerWeek} פריטים לאורך כל התכנית).

**כללי שיבוץ:**
- פזרי בין הקטגוריות (בעל ערך / אישי / מכירתי / בידורי) לאורך כל שבוע - אל תרכזי את כל אותה קטגוריה יחד, בערך ביחס 80/20 שרוב התוכן בעל-ערך/אישי ומיעוטו מכירתי/בידורי, ככל שהרעיונות הקיימים מאפשרים זאת.
- כל רעיון מהמאגר משמש **פעם אחת בלבד** בכל התכנית כולה - אסור לחזור על אותו רעיון פעמיים.
- אסור להמציא רעיונות חדשים - לשבץ אך ורק מתוך הרשימה שסופקה, עם הכותרת בדיוק כפי שהיא מופיעה שם.
- **אם אין מספיק רעיונות מתאימים כדי למלא את כל היעד** - שבצי כמה שיש בצורה הגיונית, השאירי את שאר הימים באותו שבוע ריקים (בלי item), וכתבי בשדה "note" של אותו שבוע משהו כמו "יש עוד מקום פנוי השבוע - כדאי להוסיף עוד רעיונות עם קטגוריה מוגדרת כדי למלא את זה". אל תמלאי במקום זה עם תוכן מומצא.

הרעיונות הזמינים מהמאגר (עם קטגוריה ושלב שכנוע כפי שכבר סווגו):
${ideasBlock}
${liveBlock}

${CONCISENESS_RULE}

השב/י אך ורק ב-JSON תקין, בלי שום טקסט נוסף לפני או אחרי, ובלי markdown code fences, בפורמט המדויק הבא:
{"weeks":[{"label":"שבוע 1","note":"","items":[{"day":"יום ראשון","type":"app","ideaTitle":"<בדיוק כמו שכתוב ברשימה למעלה, אות באות>","category":"<הקטגוריה שלו>"},{"day":"יום שלישי","type":"live","note":"<תיאור קצר של הפריט החי>"}]}, ... בדיוק ${weeksCount} שבועות בסך הכל]}`;
}

module.exports = { buildContentPlanPrompt };
