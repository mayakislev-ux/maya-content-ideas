const WARMING_METHODOLOGY = `
סיכום - חימום קהל בסטורי

מה זה חימום קהל? חימום קהל = לגרום לאנשים לרצות לקנות מכם, עוד לפני שדיברתם על מכירה. בממוצע, קהל צריך 3-6 חודשים כדי לעבור מצפייה לרכישה.

סוגי קהל ברשת: קהל קר - הגיע מרילס או מפרסום, עוד לא מכיר אתכם ולא יודע מה אתם מוכרים. קהל חם - עוקב קבוע שמכיר, סומך ורוצה מכם עוד. המטרה: להפוך קהל קר לחם דרך החימום.

אסור "להיזכר" בחימום רק לפני מכירה - הוא צריך להיות חלק קבוע מהתוכן השבועי.

שני סוגי חימום, שתי מטרות שונות:
חימום שוטף = קורה באופן יומיומי, גורם לקהל לרצות לקנות מכם מרוב חיבור ואמון, לא מכוון למכירה אלא לבניית מערכת יחסים אמיתית.
חימום לקראת מכירה = קורה לקראת הצעה מסוימת, גורם לקהל להבין שהוא צריך את המוצר עכשיו.

--- חימום שוטף - למכור בלי למכור ---
קורה כל יום, גם בלי סיבה. לא מכוון למכירה אבל יוצר רצון לקנות. נועד לבנות מערכת יחסים אמיתית עם הקהל.

שלושת החוקים של חימום שוטף:

1. חוק הפער - תראו שיש לכם משהו שהקהל רוצה. המטרה: לגרום לקהל לרצות, לא לקנא.
- פער ישיר - תוצאה שהקהל רוצה בעצמו שלכם יש (למשל: מטפלת רגשית - שקט/שלווה/פחות חרדות; מעצבת שיער - שיער חלק, מראה מושלם בלי להתעסק).
- פער עקיף - מראה את סגנון החיים או התחושה שהשירות מאפשר (למשל: להרגיש חופשי לבחור, זמן פנוי בבוקר, ביטחון עצמי).

2. חוק ההשתקפות - תגרמו להם לראות את עצמם בתוכן שלכם, תדברו את השפה שלהם, תראו שגם אתם הייתם שם. שאלות מנחות: מה הלקוחות מרגישים כשהם נתקעים? אילו מחשבות רצות להם בראש ביום רגיל? מה המשפטים הכי חוזרים ששומעים מהם? מה הבעיות/כאבים שלהם? מה אני בעצמי הרגשתי כשהייתי במקום שלהם? מה הם חושבים על עצמם כשהם לא מצליחים? על מה הם מתביישים לדבר בקול? אילו אמונות מגבילות יש להם?

3. חוק הראי - תראו שזה אפשרי גם בשבילם, דרך סיפור שלכם או סיפור לקוח. אל תראו רק תוצאה סופית - תראו את הדרך ואת נקודת ההתחלה, את הקושי בדרך להצלחה. תגרמו לצופה להרגיש "הם היו בדיוק כמוני". שאלות מנחות: מי עבר תהליך איתי והגיע לתוצאה שמישהו יוכל להזדהות איתה? איזו תוצאה של לקוח מדברת בעד עצמה? איך אפשר להראות את הדרך של הלקוח, לא רק את הסוף? מה הלקוח הרגיש לפני השינוי? מה הוא חשב שלא יקרה - ובסוף קרה? מה היה השלב הכי קשה בתהליך ואיך הוא עבר אותו? איזה משפט הוא אמר אחרי שהצליח, שמייצג את כל הסיפור? איך אפשר להראות שהשיטה פשוטה ולא תלויה באדם עצמו?

בכל שבוע, ודאו ששלושת האלמנטים האלו הופיעו בסטורי: חיבור (גורם להם להרגיש שאתם מבינים אותם), רצון (גורם להם לרצות את מה שיש לכם), אמונה (גורם להם לדעת שזה אפשרי גם בשבילם).

כחלק מחימום שוטף, חשוב גם:
- קבלת פנים לעוקבים חדשים - אם נוספו הרבה עוקבים חדשים בבת אחת, לעשות להם קבלת פנים שמסבירה מי אתם, מה הערך שאתם נותנים ולמה כדאי להישאר עוקבים פעילים.
- הראו שיש למוצר/לשירות ביקוש גבוה - למשל שיתוף צילומי מסך של פניות שהתקבלו מלקוחות (גם אם מעט), יוצר רצון לרכוש גם אצל הצד השני.

--- חימום לקראת מכירה ---
השלב שמגיע לפני כל מכירה. נמשך בממוצע 5-7 ימים. קורה אחת לחודש/שבועיים, לפני כל סבב מכירה חדש.
המטרה: לגרום לקהל לזהות בעיה, להבין שהמצב הנוכחי לא עובד, ולהרגיש שמה שאתם מציעים זה הפתרון הכי נכון להם - עוד לפני שהצעתם בפועל.

שלבי החימום לפני מכירה:
שלב ההכנה (מודעות לבעיה) - 1-2 ימים: מציפים בעיות נפוצות של הקהל, חושפים טעויות שהקהל עושה בלי לדעת, גורמים להם להגיד בראש "וואלה זה בדיוק אני".
שלב החימום העיקרי (מודעות לפתרון) - 2-3 ימים: השוואה בין השירות לפתרונות אחרים בשוק (שריפת גשרים), ניפוץ אמונות מגבילות שקיימות לגבי השירות/מוצר, הצגת הצלחות לקוחות (לפני/אחרי/תוצאות/עדויות), ביקורת על הקהל/על בעלי עסקים אחרים בתחום.
שלב סגירת המכירה - 1-2 ימים: הסדר הלוגי של יום מכירה בסטורי -
סטורי 1 - עצירה (תמונה/רקע בצבע עם משפט שמסקרן ועוצר גלילה, כמו "תעצרו הכל")
סטורי 2 - הוכחה חברתית של לקוח (הקלטה/הודעה/סרטון + משפט שממנף את התוצאה)
סטורי 3 - חיזוק התוצאה (דיבור למצלמה שנותן עוד משקל לאותה תוצאה, מתרכז בבעיה/פחד אחד ולא מתפזר)
סטורי 4 - הנעה לפעולה + טריגר קונקרטי (הטבה, בונוס, טריגר רגשי) שגורם להשאיר פרטים דווקא עכשיו

מה קורה אחרי שסגרנו מכירה? רוב בעלי העסקים נעלמים אחרי המכירה - המומחים מחממים הכי חזק דווקא אחרי שסוגרים: מציגים לקוחות בפעולה (תמונות/וידאו/מאחורי הקלעים), משתפים הצלחות ותובנות של משתתפים קיימים, מדברים על מה שקורה עכשיו בתהליך (לא רק מה שהיה), יוצרים תחושת "פספוס חיובי" (FOMO) - "תראו מה הם חווים עכשיו".

דוגמה אמיתית (בעלת אקדמיה של מיקרובליידינג, 7 ימים של חימום לקראת מכירה):
יום 1: הצפת בעיה - משכורת שלא מספיקה לחיות במדינה
יום 2: הצפת בעיה - לעבוד בעבודה שלא אוהבים
יום 3: שריפת גשר - השוואה לקורס לק ג'ל
יום 4: הוכחות חברתיות - הצלחה של תלמידה (נופר)
יום 5: ניפוץ אמונה מגבילה - "יש הרבה בתחום, למה דווקא אני אצליח?"
יום 6: מכירה
יום 7: יצירת פומו

לסיכום: חימום שוטף = בניית חיבור ואמון יומיומי. חימום לפני מכירה = בניית רצון וציפייה. חימום אחרי מכירה = שימור אנרגיה ויצירת פספוס.
`.trim();

function buildWarmingPrompt({ product, audience, extraContext, existingIdeasTitles }) {
  const ideasContext =
    existingIdeasTitles && existingIdeasTitles.length
      ? `\n\nרעיונות לתוכן שכבר קיימים במאגר של המשתמשת (רק כדי להכיר את הסגנון והטון שלה - לא חובה להתבסס עליהם):\n${existingIdeasTitles.map((t) => `- ${t}`).join('\n')}`
      : '';

  const extra =
    extraContext && extraContext.trim()
      ? `\n\nמידע נוסף שסיפקה המשתמשת על הקהל/המוצר:\n${extraContext.trim()}`
      : '';

  return `את/ה עוזר/ת תוכן שבונה תוכנית חימום קהל לסטוריז עבור בעלת עסק (לקוחת "אקדמיית המהלך השיווקי"), בהתאם למתודולוגיה המדויקת הבאה של מאיה קיסלב. השתמש/י אך ורק במתודולוגיה הזו - אל תמציא/י כללים משלך.

${WARMING_METHODOLOGY}

המוצר/ההצעה שמקדמים בסבב החימום הזה: ${product}
קהל היעד שעליו מתמקד הסבב הזה: ${audience}${extra}${ideasContext}

המשימה שלך: לבנות תוכנית חימום לסטוריז ל-3 שבועות (21 ימים בסה"כ), בהתאם למבנה הבא:

שבוע 1 ושבוע 2 = חימום שוטף (יומיומי, לא ממוקד מכירה):
- כל יום - רעיון קונקרטי אחד לסטורי (לא סקריפט, רק הרעיון/הכיוון בכמה מילים), מבוסס על אחד משלושת החוקים (חוק הפער - ישיר או עקיף, חוק ההשתקפות, חוק הראי). פזר/י בין שלושת החוקים לאורך השבועיים, בלי לחזור על אותו חוק יומיים ברצף.
- ודא/י שבסוף כל שבוע (7 ימים) הופיעו בפועל שלושת האלמנטים: חיבור, רצון, אמונה.
- שלב/י פעם אחת באחד משני השבועות יום "קבלת פנים לעוקבים חדשים", ופעם אחת יום "הוכחת ביקוש" (שיתוף/אזכור פניות שהתקבלו) - רק אם זה טבעי, לא חובה שיהיה בשני השבועות.
- כל רעיון צריך להיות ספציפי לקהל היעד ולמוצר שצוינו למעלה, לא כללי.

שבוע 3 = חימום לקראת מכירה (7 ימים, בהתאם למבנה שלושת השלבים ולדוגמה של מאיה למעלה):
יום 1: הצפת בעיה נפוצה של הקהל בהקשר של המוצר/השירות
יום 2: הצפת בעיה/טעות נוספת (המשך שלב ההכנה)
יום 3: שריפת גשר - השוואה לפתרון אחר שהקהל שוקל, ולמה הוא לא מספיק
יום 4: הוכחה חברתית - תוצאה/עדות של לקוח
יום 5: ניפוץ אמונה מגבילה שיש לקהל לגבי המוצר/השירות/התחום
יום 6: יום המכירה - כתוב/כתבי כאן את כל 4 הביטים של יום הסגירה יחד, ממוספרים בתוך הטקסט: (1) עצירה - משפט/וויזואל שעוצר גלילה, (2) הוכחה חברתית של לקוח + משפט שממנף את התוצאה, (3) חיזוק התוצאה - דיבור למצלמה שמעמיק על אותה תוצאה/בעיה אחת, (4) הנעה לפעולה עם טריגר קונקרטי (הטבה/בונוס/רגש) שמניע להשאיר פרטים עכשיו
יום 7: המשך חימום אחרי מכירה - יצירת תחושת "פספוס" (FOMO) למי שלא נרשם/רכש, בלי להיעלם אחרי המכירה

השב/י אך ורק ב-JSON תקין בפורמט הבא, בלי שום טקסט נוסף לפני או אחרי, ובלי markdown code fences:
{"week1":[{"day":"יום ראשון","idea":"..."},{"day":"יום שני","idea":"..."},{"day":"יום שלישי","idea":"..."},{"day":"יום רביעי","idea":"..."},{"day":"יום חמישי","idea":"..."},{"day":"יום שישי","idea":"..."},{"day":"יום שבת","idea":"..."}],"week2":[... 7 ימים באותו פורמט ...],"week3":[... 7 ימים באותו פורמט, בהתאם למבנה השבוע השלישי שתואר למעלה ...]}

כל שדה "idea" צריך להיות כתוב בעברית פשוטה, ישירה וקונקרטית (בסגנון של מאיה קיסלב - ישיר, חם, בלי מטאפורות מומצאות, בלי סיסמאות כלליות) - רעיון שאפשר לעצום עיניים ולדמיין אותו כסטורי אמיתי, לא כותרת מופשטת. אורך כל idea: 1-3 משפטים קצרים.`;
}

module.exports = { buildWarmingPrompt };
